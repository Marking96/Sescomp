        <h3>O que é <span>Software Livre</span>?</h3>

        <p><a href="http://softwarelivre.org">Software Livre</a> é uma forma de manifestação de um software em que, resumidamente, permite-se adaptações ou modificações em seu código de forma espontânea, ou seja, sem que haja a necessidade de solicitar permissão ao seu proprietário para modificá-lo. Não confundir com o movimento Open Source.</p>

